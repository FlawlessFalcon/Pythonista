'''
The standard arithmetic operations, +, -, *, /, and ** (exponentiation), can be used with parentheses forcing the order of operations away
from normal operator precedence. Other very useful operations are the remainder (modulo) operator, %, and integer division, //. Note that when
two integers are divided, the result is a floating point. The integer division operator returns the integer portion of the quotient by truncating
any fractional part.

'''

__author__ = "Ganesh"

print(2+3*4)
print((2+3)*4)
print(2**10)
print(6/3)
print(7/3)
print(7//3)
print(7%3)
print(3/6)
print(3//6)
print(3%6)
print(2**100)
